    function ValidateRegistrationSubmission()
    {
        //This is a hidden input field. Humans won't fill this field.
        if(!empty($_POST[$this->GetSpamTrapInputName()]) )
        {
            //The proper error is not given intentionally
            $this->HandleError("Automated submission prevention: case 2 failed");
            return false;
        }
        
        $validator = new FormValidator();
        $validator->addValidation("fa_name","req","Please fill in Name");
        $validator->addValidation("fa_address","req","Please enter an address");
        $validator->addValidation("fa_city","req","Please enter a city");
        $validator->addValidation("fa_state","req","Please enter a state");
        $validator->addValidation("fa_zip","req","Please enter a zipcode");
        $validator->addValidation("fa_phone_home","req","Please enter a phone #");
        $validator->addValidation("mo_name","req","Please fill in Name");
        $validator->addValidation("mo_address","req","Please enter an address");
        $validator->addValidation("mo_city","req","Please enter a city");
        $validator->addValidation("mo_state","req","Please enter a state");
        $validator->addValidation("mo_zip","req","Please enter a zipcode");
        $validator->addValidation("mo_phone_home","req","Please enter a phone #");
        
        if(!$validator->ValidateForm())
        {
            $error='';
            $error_hash = $validator->GetErrors();
            foreach($error_hash as $inpname => $inp_err)
            {
                $error .= $inpname.':'.$inp_err."\n";
            }
            $this->HandleError($error);
            return false;
        }        
        return true;
    }
    
